let primeiroNumero = prompt("Primeiro Número: ")

let segundoNumero = prompt("Segundo Número: ")

primeiroNumero = parseFloat(primeiroNumero)
segundoNumero = parseFloat(segundoNumero)

let sub = primeiroNumero - segundoNumero

let adi = primeiroNumero + segundoNumero

let multi = primeiroNumero * segundoNumero

let divi = primeiroNumero / segundoNumero

alert("Subtração: "+ sub + " " + "\nAdição: " + adi + " " + "\nMultiplicação: " + multi + " " + "\nDivisão: " + divi)